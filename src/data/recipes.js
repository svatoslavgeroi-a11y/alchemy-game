// Кешируем список нетерминальных элементов для быстрой проверки
const nonTerminalElements = new Set();
for (const recipeKey of Object.keys(RECIPES)) {
  recipeKey.split('+').forEach(id => nonTerminalElements.add(id));
}

export function isTerminalElement(id) {
  return !nonTerminalElements.has(id);
}

export function validateRecipes(elementsData) {
  let hasErrors = false;
  for (const [recipeKey, result] of Object.entries(RECIPES)) {
    const ingredients = recipeKey.split('+');
    ingredients.forEach(ing => {
      if (!elementsData[ing]) {
        console.warn(`[Validation Warning] Recipe ingredient "${ing}" in recipe "${recipeKey}" does not exist in ELEMENTS_DATA.`);
        hasErrors = true;
      }
    });

    const resultIds = Array.isArray(result) ? result : [result];
    resultIds.forEach(res => {
      if (!elementsData[res]) {
        console.warn(`[Validation Warning] Recipe result "${res}" in recipe "${recipeKey}" does not exist in ELEMENTS_DATA.`);
        hasErrors = true;
      }
    });
  }
  if (!hasErrors) {
    console.log('[Validation] All recipes are valid.');
  }
}
